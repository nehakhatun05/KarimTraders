// Helper functions
export function getProductsByCategory(categorySlug: string): Product[] {
  return products.filter(p => p.category.slug === categorySlug);
}

export function getProductBySlug(slug: string): Product | undefined {
  return products.find(p => p.slug === slug);
}

export function getFeaturedProducts(): Product[] {
  return products.filter(p => p.isFeatured);
}

export function getBestSellers(): Product[] {
  return products.filter(p => p.isBestSeller);
}

export function getNewArrivals(): Product[] {
  return products.filter(p => p.isNewArrival);
}

export function searchProducts(query: string): Product[] {
  const lowerQuery = query.toLowerCase();
  return products.filter(p => 
    p.name.toLowerCase().includes(lowerQuery) ||
    p.description.toLowerCase().includes(lowerQuery) ||
    p.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
}

export function filterProducts(filters: Partial<{
  category: string;
  subcategory: string;
  minPrice: number;
  maxPrice: number;
  isOrganic: boolean;
  minRating: number;
  inStock: boolean;
}>): Product[] {
  return products.filter(p => {
    if (filters.category && p.category.slug !== filters.category) return false;
    if (filters.subcategory && p.subcategory !== filters.subcategory) return false;
    if (filters.minPrice && p.price < filters.minPrice) return false;
    if (filters.maxPrice && p.price > filters.maxPrice) return false;
    if (filters.isOrganic !== undefined && p.isOrganic !== filters.isOrganic) return false;
    if (filters.minRating && p.rating < filters.minRating) return false;
    if (filters.inStock && p.stockStatus === 'out-of-stock') return false;
    return true;
  });
}
